function changeDocument(delta) {
    let newIndex = currentDocIndex + delta;
    if (newIndex < 0) newIndex = currentDocArray.length - 1;
    if (newIndex >= currentDocArray.length) newIndex = 0;
    currentDocIndex = newIndex;
    renderDocumentGallery();
}

