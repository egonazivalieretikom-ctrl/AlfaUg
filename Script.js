function runCalc() {
    const area = document.getElementById('calc-area').value || 0;
    const surface = document.getElementById('calc-surface').value;
    const layers = document.getElementById('calc-layers').value;
    const paintSelect = document.getElementById('calc-paint');
    let baseConsumption = 0.15; // кг/м² по умолчанию

    if (paintSelect && paintSelect.value) {
        const productId = parseInt(paintSelect.value);
        const product = products.find(p => p.id === productId);
        if (product && product.consumption) {
            baseConsumption = product.consumption;
        }
    }

    const total = (area * baseConsumption * surface * layers).toFixed(1);
    document.getElementById('res-total').innerText = total;
}

