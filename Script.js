let estimateItems = [];
const ESTIMATE_STORAGE_KEY = 'alfa_estimate';

// Загрузка сметы из localStorage при инициализации
function loadEstimate() {
    const saved = localStorage.getItem(ESTIMATE_STORAGE_KEY);
    if (saved) {
        try {
            estimateItems = JSON.parse(saved);
            // Если у элемента нет unit, добавляем 'kg' (для старых данных)
            estimateItems.forEach(item => {
                if (!item.unit) item.unit = 'kg';
                // Можно также удалить поле price, если оно есть (необязательно)
                // delete item.price;
            });
        } catch (e) {
            estimateItems = [];
        }
    } else {
        estimateItems = [];
    }
    updateEstimateCount();
}

// Сохранение сметы в localStorage
function saveEstimate() {
    localStorage.setItem(ESTIMATE_STORAGE_KEY, JSON.stringify(estimateItems));
    updateEstimateCount();
}

// Обновление счётчика на иконке
function updateEstimateCount() {
    const countEl = document.getElementById('estimate-count');
    if (countEl) {
        const totalItems = estimateItems.reduce((sum, item) => sum + item.quantity, 0);
        countEl.textContent = Math.ceil(totalItems); // округление вверх
    }
}

// Добавление товара в смету
function addToEstimate(productId) {
    const product = products.find(p => p.id === productId);
    if (!product) return;

    const existing = estimateItems.find(item => item.id === productId);
    if (existing) {
        existing.quantity += 1;
    } else {
        estimateItems.push({
            id: product.id,
            name: product.name,
            image: product.image,
            quantity: 1,
            unit: 'kg' // по умолчанию килограммы
        });
    }
    saveEstimate();
}



function addCalculationToEstimate() {
    const totalElement = document.getElementById('res-total');
    if (!totalElement) return;
    const volume = parseFloat(totalElement.innerText);
    if (isNaN(volume) || volume <= 0) {
        alert('Сначала выполните расчёт (введите площадь)');
        return;
    }

    const area = parseFloat(document.getElementById('calc-area').value) || 0;
    const surfaceSelect = document.getElementById('calc-surface');
    const surfaceFactor = parseFloat(surfaceSelect.value);
    const surfaceText = surfaceSelect.options[surfaceSelect.selectedIndex].text;
    const layersSelect = document.getElementById('calc-layers');
    const layers = parseInt(layersSelect.value);
    const layersText = layersSelect.options[layersSelect.selectedIndex].text;

    let paintId = null;
    let paintName = '';
    const paintSelect = document.getElementById('calc-paint');
    if (paintSelect && paintSelect.value) {
        paintId = parseInt(paintSelect.value);
        const product = products.find(p => p.id === paintId);
        if (product) paintName = product.name;
    }

    let baseConsumption = 0.15;
    if (paintId) {
        const product = products.find(p => p.id === paintId);
        if (product && product.consumption) baseConsumption = product.consumption;
    }

    calculationCounter++;
    const shortName = `Расчёт №${calculationCounter}`;
    const calcId = 'calc_' + Date.now();

    const calculationItem = {
        id: calcId,
        name: shortName,               // ← упрощённое название
        quantity: volume,
        unit: 'kg',
        isCalculation: true,
        area: area,
        surfaceFactor: surfaceFactor,
        layers: layers,
        paintId: paintId,
        baseConsumption: baseConsumption,
        paintName: paintName
    };

    estimateItems.push(calculationItem);
    saveEstimate();
    alert('Расчёт добавлен в смету');

    const modal = document.getElementById('modal');
    if (modal.style.display === 'flex') {
        renderEstimateModal();
    }
}

// Увеличение количества
function increaseEstimateItem(itemId) {
    const item = estimateItems.find(item => item.id == itemId); // используем нестрогое сравнение
    if (item) {
        item.quantity += (item.isCalculation ? 0.1 : 1);
        // Округляем до 1 знака для расчётов
        if (item.isCalculation) item.quantity = Math.round(item.quantity * 10) / 10;
        saveEstimate();
        renderEstimateModal();
    }
}

// Уменьшение количества (если станет 0 – удаляем)
function decreaseEstimateItem(itemId) {
    const item = estimateItems.find(item => item.id == itemId);
    if (item) {
        const step = item.isCalculation ? 0.1 : 1;
        if (item.quantity > step) {
            item.quantity -= step;
            if (item.isCalculation) item.quantity = Math.round(item.quantity * 10) / 10;
            saveEstimate();
        } else {
            removeEstimateItem(itemId);
            return;
        }
        renderEstimateModal();
    }
}

// Удаление товара из сметы
function removeEstimateItem(itemId) {
    estimateItems = estimateItems.filter(item => item.id != itemId); // нестрогое сравнение
    saveEstimate();
    renderEstimateModal();
}

