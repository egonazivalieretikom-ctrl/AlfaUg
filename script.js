let lastProduct = null;               // последний открытый товар
let previousModalState = null;         // состояние для возврата (например, { type: 'product', product: ... })

// ==================== МОДАЛЬНОЕ ОКНО ТОВАРА ====================
function openProductModal(product) {
    lastProduct = product;              // запоминаем товар
    previousModalState = null;          // сбрасываем предыдущее состояние (теперь мы в карточке)

    const modal = document.getElementById('modal');
    const body = document.getElementById('modal-body');
    modal.style.display = 'flex';

    body.innerHTML = `
        <div class="product-modal-content">
            <div class="product-modal-image">
                <img src="${product.image}" alt="${product.name}" class="product-main-image" style="cursor: pointer;">
            </div>
            <div class="product-modal-info">
                <h3>${product.name}</h3>
                <div class="product-full-description">${product.fullDescription || product.description}</div>
                <div class="product-modal-actions">
                    <button class="btn-primary" onclick="addToEstimate(${product.id})">Добавить в смету</button>
                    <button class="btn-secondary" onclick="closeModal()">Закрыть</button>
                </div>
            </div>
        </div>
    `;

    // Обработчик клика на изображение
    const productImage = body.querySelector('.product-main-image');
    if (productImage) {
        productImage.addEventListener('click', (e) => {
            e.stopPropagation();
            openImageModal(product.image);
        });
    }
}

// ==================== МОДАЛЬНОЕ ОКНО УВЕЛИЧЕННОГО ФОТО ====================
function openImageModal(imageSrc) {
    // Запоминаем, что мы были в карточке товара
    if (lastProduct) {
        previousModalState = { type: 'product', product: lastProduct };
    }

    const modal = document.getElementById('modal');
    const body = document.getElementById('modal-body');
    modal.style.display = 'flex';

    body.innerHTML = `
        <div class="image-modal-content">
            <img src="${imageSrc}" alt="Увеличенное фото" class="enlarged-image">
        </div>
    `;

    // Добавляем обработчик клика на само изображение
    const enlargedImage = body.querySelector('.enlarged-image');
    if (enlargedImage) {
        enlargedImage.addEventListener('click', (e) => {
            e.stopPropagation(); // предотвращаем всплытие к фону
            closeModal(); // возвращаемся к карточке товара
        });
    }
}

