let currentPage = 1;
const itemsPerPage = 6; // количество товаров на одной странице
let filteredProducts = products; // текущий отфильтрованный массив

// ==================== ОТРИСОВКА КАТАЛОГА ====================
function renderCatalog(productsArray) {
    const grid = document.getElementById('productGrid');
    if (!grid) return;

    grid.innerHTML = ''; // очищаем сетку

    productsArray.forEach(product => {
        const card = document.createElement('div');
        card.className = 'product-card';
        card.dataset.category = product.category;

        const badgeHTML = product.badge ? `<div class="product-badge">${product.badge}</div>` : '';

        // Создаём HTML структуру карточки без onclick атрибутов
        card.innerHTML = `
            ${badgeHTML}
            <img src="${product.image}" alt="${product.name}">
            <h3>${product.name}</h3>
            <p>${product.description}</p>
            <div class="product-footer">
                <button class="spec-btn">Спецификация</button>
                <button class="estimate-btn">Добавить в смету</button>
            </div>
        `;

        // Обработчик клика по карточке (исключая кнопки)
        card.addEventListener('click', (e) => {
            if (e.target.tagName === 'BUTTON') return; // игнорируем клики по кнопкам
            openProductModal(product);
        });

        // Обработчик кнопки "Спецификация"
        const specBtn = card.querySelector('.spec-btn');
        specBtn.addEventListener('click', (e) => {
            e.stopPropagation(); // предотвращаем всплытие к карточке
            openProductModal(product);
        });

        // Обработчик кнопки "Добавить в смету"
        const estimateBtn = card.querySelector('.estimate-btn');
        estimateBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            addToEstimate(product.id);
        });

        grid.appendChild(card);
    });
}

// ==================== ОБНОВЛЕНИЕ КАТАЛОГА С УЧЁТОМ ПАГИНАЦИИ ====================
function updateCatalog() {
    const totalItems = filteredProducts.length;
    const totalPages = Math.ceil(totalItems / itemsPerPage);

    if (currentPage < 1) currentPage = 1;
    if (currentPage > totalPages && totalPages > 0) currentPage = totalPages;

    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    const productsToShow = filteredProducts.slice(startIndex, endIndex);

    renderCatalog(productsToShow);
    renderPaginationControls(totalItems, currentPage);
}

// ==================== ОТРИСОВКА ЭЛЕМЕНТОВ ПАГИНАЦИИ ====================
function renderPaginationControls(totalItems, currentPage) {
    const paginationDiv = document.getElementById('pagination');
    if (!paginationDiv) return;

    const totalPages = Math.ceil(totalItems / itemsPerPage);

    if (totalPages <= 1) {
        paginationDiv.innerHTML = '';
        return;
    }

    let html = '<div class="pagination-controls">';
    html += `<button class="pagination-btn" ${currentPage === 1 ? 'disabled' : ''} onclick="changePage(${currentPage - 1})">‹</button>`;

    for (let i = 1; i <= totalPages; i++) {
        if (i === currentPage) {
            html += `<span class="pagination-current">${i}</span>`;
        } else {
            html += `<button class="pagination-btn" onclick="changePage(${i})">${i}</button>`;
        }
    }

    html += `<button class="pagination-btn" ${currentPage === totalPages ? 'disabled' : ''} onclick="changePage(${currentPage + 1})">›</button>`;
    html += '</div>';
    paginationDiv.innerHTML = html;
}

// ==================== СМЕНА СТРАНИЦЫ ====================
function changePage(page) {
    const totalPages = Math.ceil(filteredProducts.length / itemsPerPage);
    if (page < 1 || page > totalPages) return;
    currentPage = page;
    updateCatalog();
    document.getElementById('catalog').scrollIntoView({ behavior: 'smooth' });
}

// ==================== ФИЛЬТРАЦИЯ ====================
function filterProducts(rusCategory) {
    const btns = document.querySelectorAll('.filter-btn');
    btns.forEach(btn => btn.classList.remove('active'));

    const activeBtn = Array.from(btns).find(btn => btn.textContent.trim() === rusCategory);
    if (activeBtn) activeBtn.classList.add('active');

    const categoryMap = {
        'Все': 'all',
        'Интерьерные': 'interior',
        'Фасадные': 'facade',
        'Грунты': 'ground'
    };
    const category = categoryMap[rusCategory] || 'all';

    if (category === 'all') {
        filteredProducts = products;
    } else {
        filteredProducts = products.filter(p => p.category === category);
    }

    currentPage = 1;
    updateCatalog();
}

// ==================== КАЛЬКУЛЯТОР ====================
function runCalc() {
    const area = document.getElementById('calc-area').value || 0;
    const surface = document.getElementById('calc-surface').value;
    const layers = document.getElementById('calc-layers').value;
    const baseConsumption = 0.15;

    const total = (area * baseConsumption * surface * layers).toFixed(1);
    document.getElementById('res-total').innerText = total;
}

