// ✅ Правильная версия closeModal (с возвратом к предыдущему состоянию)
function closeModal() {
    if (previousModalState) {
        if (previousModalState.type === 'product') {
            openProductModal(previousModalState.product);
        }
        previousModalState = null;
    } else {
        document.getElementById('modal').style.display = 'none';
    }
}

